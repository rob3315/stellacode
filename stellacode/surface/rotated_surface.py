class RotatedSurface(eqx.Module):
    """ """

    surface: AbstractSurfaceFactory
    rotate_n: RotateNTimes = RotateNTimes(1)

    def __call__(self, surface: Surface, key=None):
        """ """
        new_surface = Surface(
            integration_par=surface.integration_par,
            grids=surface.grids,
        )
        for k in [
            "xyz",
            "jac_xyz",
            "normal",
            "ds",
            "normal_unit",
            "hess_xyz",
            "principle_max",
            "principle_min",
            "j_surface",
        ]:
            stack_dim = None
            if k in dir(surface):
                if k == "j_surface":
                    stack_dim = 1
                val = getattr(surface, k)
                if val is not None:
                    setattr(new_surface, k, self.rotate_n(val, stack_dim=stack_dim))
            return new_surface


